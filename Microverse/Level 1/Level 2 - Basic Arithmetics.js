//Slide 8 - Basic Arithmetics
//Plus
let nPlus = 5;
console.log(nPlus);

nPlus = nPlus + 3;
console.log(nPlus);
//or
nPlus += 3;
console.log(nPlus);

//add 1 unit
nPlus ++;
nPlus ++;
nPlus ++;

//Minus
let nMinus = 5;
console.log(nMinus);

nMinus = nMinus - 3;
console.log(nMinus);
//or
nMinus -= 3;
console.log(nMinus);

//minus 1 unit
nPlus --;
nPlus --;
nPlus --;

//Multiply
let nMultiply = 5;
console.log(nMultiply);

nMultiply = nMultiply * 3;
console.log(nMultiply);
//or
nMultiply *= 3;
console.log(nMultiply);

//Divide
let nDivide = 5
console.log(nDivide);

nDivide = nDivide * 3;
console.log(nDivide);
//or
nDivide /= 3;
console.log(nDivide);
